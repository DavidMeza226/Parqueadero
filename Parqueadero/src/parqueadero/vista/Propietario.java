    public void cargarDatos() {

        if (duenio != null) {
            textCedula.setText(duenio.getCedula());
            textNombre.setText(duenio.getNombre());
            textApellido.setText(duenio.getApellido());
            textContacto.setText(duenio.getContacto());
        } else {
            textCedula.setText("");
            textNombre.setText("");
            textApellido.setText("");
            textContacto.setText("");
        }

    }

